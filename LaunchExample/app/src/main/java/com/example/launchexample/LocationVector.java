public class LocationVector {
    public double x;
    public double y;
    public double vx;
    public double vy;
    public double ax;
    public double ay;

    public LocationVector(double x, double y, double vx, double vy, double ax, double ay)
    {
        this.x = x;
        this.y = y;
        this.vx = vx;
        this.vy = vy;
        this.ax = ax;
        this.ay = ay;
    }
}
